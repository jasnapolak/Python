mood = input(str("What's your mood? (Answer with one word): "))

if mood == "happy":
    print("It is great to see you happy!")

elif mood == "nervous":
    print("Take a deep breath 3 times.")

elif mood == "sad":
    print("There is always sunshine after rain.")

elif mood == "excited":
    print("Me too :D")

elif mood == "relaxed":
    print("Enjoy, it's weekend.")

else:
    print("I don't recognize this mood.")